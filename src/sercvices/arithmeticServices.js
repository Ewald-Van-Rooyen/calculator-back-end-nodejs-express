function addition(x, y) {
    return x + y;
}

function subtraction(x, y) {
    return x - y;
}

function multiplication(x, y) {
    return x * y;
}

function division(x, y) {
    return x / y;
}

function modulation(x, y) {
    return x % y;
}

module.exports = {addition, subtraction, multiplication, division, modulation};
